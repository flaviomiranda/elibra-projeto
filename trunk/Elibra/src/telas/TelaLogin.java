    private void btnLogarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLogarActionPerformed
        // TODO add your handling code here:
        String login = txtLogin.getText();
        String senha = new String(txtSenha.getPassword());
        if(login.equalsIgnoreCase(""))
            JOptionPane.showMessageDialog(null, "Obrigatório informar o Login");
        else
            if(senha.equalsIgnoreCase(""))
                JOptionPane.showMessageDialog(null, "Obrigatório informar a Senha");
            else
            {
                DaoLogin daologin = new DaoLogin();
                if(daologin.validarLogin(login, senha))
                {
                    TelaMenu m = new TelaMenu(login);
                    this.dispose();
                    m.setLocationRelativeTo(null);
                    m.setVisible(true);
                }
                else{
                    JOptionPane.showMessageDialog(null, "Senha/Login Invalido(s)");
                }

            }
    }//GEN-LAST:event_btnLogarActionPerformed

